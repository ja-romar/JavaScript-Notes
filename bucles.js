/* Bucle while para iterar */

/* Podemos usar un mismo codigo multiples veces si suamos un bucle */

// El bucle While, este ejecuta una condicion especifica
// mientras sea verdadera, se detiene una vez que la condicon
// ya no es verdadera.

// Setup
const myArray = [];

// Only change code below this line.
let i = 5;
while(i >= 0) {
  myArray.push(i);
  i--;
}

