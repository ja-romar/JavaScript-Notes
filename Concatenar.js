//Concatena strings con el operador plus o mas

/* En JS cuando se utiliza el operador + en un valor de 
string o cadena se llama operador de concatenacion, de esta 
forma puedes construir una nueva string a partir de otras 
juntandolas.

-Por ejemplo */

const eConca = "First string. " +"Second string";

/*----------------------------------------------------*/

/* Concatena strings con operador "+="

Tambien podemos usar "+=" para concatenar una cadena al final
de una variable de cadena existente.

**LA CONCATENACION no agrega espacio en las cadenas concatenadas
asi que tendras que agregar los espacios por tu cuenta */

//Por ejemplo:

let ourString = "First string. ";
// El espacio esta agregado despues del punto
ourString += "Second string.";

