/*

Los argumentos son datos que se introducen o se pasan por una funcion
cuando se define una funcion, se define tipicamente junto con uno o mas
parametros. los valores reales que son pasados por dicha funcion 
se conocen como argumentos
*/

function exampleOfArgs(a1, a2) { // los argumentos son a1 y a2
    console.log(a1 * a2);
}

