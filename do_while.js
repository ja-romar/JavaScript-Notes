/* Do while es otro tipo de bucle, se llama do while
porque primero hace d0 una pasada por el codigo denntro
del codigo sin importar que, y despues continua ejeutando
el bucle mientras while la condicion especificada sea
verdadera (true) */ 

//Ejemplo

const myArray = [];
let i = 10;

do{
    myArray.push(i);
    i++;
} while (i<5);

